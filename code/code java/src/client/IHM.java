    private void cmdOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdOKActionPerformed
        
       int index;
        DefaultTableModel m=new DefaultTableModel();
       m.setColumnIdentifiers(new String[]{"Code","Nom","Prénom"});
       index=cbF.getSelectedIndex();
       if(index==0) te=cl.getAllEtds();
       else te=cl.getEtudiants(tf.get(index-1).getCodef());
       for(Etudiant e:te)
       m.addRow(new Object[]{e.getCode(),e.getNom(),e.getPrenom()});
       table.setModel(m);
    }//GEN-LAST:event_cmdOKActionPerformed

