    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
       cl=new Client("localhost",4000);
        cbF.removeAllItems();
       tf=cl.getFilieres();
       cbF.addItem("All");
       for(Filiere f:tf) cbF.addItem(f.getNomf());
       DefaultTableModel m=new DefaultTableModel();
       m.setColumnIdentifiers(new String[]{"Code","Nom","Prénom"});
       te=cl.getAllEtds();
       for(Etudiant e:te)
       m.addRow(new Object[]{e.getCode(),e.getNom(),e.getPrenom()});
       table.setModel(m);
    }//GEN-LAST:event_formWindowOpened

